"""
polarity_engine.py — Polarity-guided search from agent disagreement

Two agents solving the same problem produce two compositions on S³.
Their agreement zones are confirmed search space.
Their disagreement zones are exactly where the problem lives.
The gap quaternion between them is a direction vector into unexplored space.

Three operations:

1. map_polarity(agent_a, agent_b) -> PolarityMap
   For each decision point, classify:
   - COHERENT: both agents agree — confirmed, skip
   - W_GAP: one agent has an action the other doesn't — existence divergence
   - RGB_GAP: same actions, different sequence — structural divergence

2. identify_frontier(polarity_map) -> list[FrontierPoint]
   Find the exact boundaries between coherent and divergent zones.
   These are the mirrors — the points where bouncing one agent's model
   into the other's blind spot produces new information.

3. synthesize_hypothesis(frontier_point, agent_a, agent_b) -> Hypothesis
   At each frontier: the gap quaternion is the transform from A's model to B's.
   Apply it to A's next known action to predict what B would do at this point.
   Intersect with what neither has tried — that intersection is the candidate.

This is not averaging the agents. It's using their disagreement geometry
to triangulate what's in the space between them.
"""

import hashlib
import numpy as np
from dataclasses import dataclass, field
from typing import Literal
import closure_sdk as closure


# ── Types ────────────────────────────────────────────────────────────────────

ZoneType = Literal["coherent", "W_gap", "RGB_gap"]


@dataclass
class Zone:
    """A contiguous region of agreement or disagreement between two agents."""
    start: int           # step index
    end: int             # step index (inclusive)
    zone_type: ZoneType
    sigma: float         # drift at this zone (0.0 = perfect agreement)
    incidents: list      # fault reports from gilgamesh
    agent_a_actions: list
    agent_b_actions: list


@dataclass
class FrontierPoint:
    """
    A boundary between a coherent zone and a gap zone.
    This is where one agent has information the other doesn't.
    The gap_vector is the algebraic direction from A's model to B's.
    """
    step: int
    zone_before: Zone    # coherent
    zone_after: Zone     # gap
    gap_vector: bytes    # the difference quaternion — direction into unexplored space
    channel: str         # "W" or "RGB"
    interpretation: str


@dataclass
class Hypothesis:
    """
    A candidate action sequence for the unexplored space.
    Generated by applying the gap_vector to each agent's known actions
    and finding the intersection of what neither has tried.
    """
    frontier_step: int
    channel: str
    agent_a_next: list   # what A does after the frontier
    agent_b_next: list   # what B does after the frontier
    candidate: list      # the hypothesis: what a third agent should try
    confidence: float    # 0.0-1.0: how well the candidate triangulates both
    reasoning: str


@dataclass
class PolarityMap:
    """
    Full characterization of the agreement/disagreement structure
    between two agents on the same problem.
    """
    level_id: str
    zones: list[Zone]
    frontiers: list[FrontierPoint]
    coherent_fraction: float   # fraction of steps where both agree
    dominant_channel: str      # "W", "RGB", or "mixed"
    total_sigma: float
    summary: str


# ── Action encoding ──────────────────────────────────────────────────────────

ACTION_NAMES = {1: "UP", 2: "DOWN", 3: "LEFT", 4: "RIGHT", 5: "ACT"}


def action_to_bytes(action: int, position: int) -> bytes:
    """Encode action + position to bytes. Position-sensitive."""
    return hashlib.sha256(f"{position}:action{action}".encode()).digest()


def action_to_bytes_positionless(action: int) -> bytes:
    """Encode action without position — for checking existence vs ordering."""
    return hashlib.sha256(f"action{action}".encode()).digest()


# ── Core: zone mapping ────────────────────────────────────────────────────────

def map_polarity(
    level_id: str,
    plan_a: list[int],
    plan_b: list[int],
    window: int = 5,
) -> PolarityMap:
    """
    Scan both plans with a sliding window, classifying each region
    as coherent or gap, and finding the frontier boundaries.

    window: number of actions per comparison segment
    """
    # Pad shorter plan
    max_len = max(len(plan_a), len(plan_b))
    a = list(plan_a) + [0] * (max_len - len(plan_a))
    b = list(plan_b) + [0] * (max_len - len(plan_b))

    zones = []
    i = 0
    while i < max_len:
        end = min(i + window, max_len)
        seg_a = a[i:end]
        seg_b = b[i:end]

        # Encode with position (order-sensitive)
        stream_a = [action_to_bytes(act, i + j) for j, act in enumerate(seg_a) if act != 0]
        stream_b = [action_to_bytes(act, i + j) for j, act in enumerate(seg_b) if act != 0]

        if not stream_a and not stream_b:
            i += window
            continue

        # Drift
        seer_a = closure.Seer()
        seer_b = closure.Seer()
        for byt in stream_a: seer_a.ingest(byt)
        for byt in stream_b: seer_b.ingest(byt)
        cmp = seer_a.compare(seer_b)
        sigma = cmp.drift

        # Fault classification
        faults = closure.gilgamesh(stream_a, stream_b)
        n_missing = sum(1 for f in faults if f.incident_type == "missing")
        n_reorder = sum(1 for f in faults if f.incident_type == "reorder")

        if sigma < 0.01:
            zone_type = "coherent"
        elif n_missing >= n_reorder:
            zone_type = "W_gap"
        else:
            zone_type = "RGB_gap"

        zones.append(Zone(
            start=i, end=end - 1,
            zone_type=zone_type,
            sigma=round(sigma, 4),
            incidents=[{"type": f.incident_type, "src": f.source_index, "tgt": f.target_index}
                       for f in faults[:3]],
            agent_a_actions=seg_a,
            agent_b_actions=seg_b,
        ))
        i += window

    # Find frontiers: coherent → gap transitions
    frontiers = []
    for idx in range(1, len(zones)):
        prev = zones[idx - 1]
        curr = zones[idx]
        if prev.zone_type == "coherent" and curr.zone_type != "coherent":
            # This is a frontier — compute gap vector
            # Gap vector = what transforms A's state into B's at this boundary
            stream_a = [action_to_bytes(act, prev.start + j)
                        for j, act in enumerate(prev.agent_a_actions) if act != 0]
            stream_b = [action_to_bytes(act, prev.start + j)
                        for j, act in enumerate(prev.agent_b_actions) if act != 0]

            # The gap vector is the composition that bridges A to B
            # We encode it as the XOR-hash of both streams (algebraic proxy)
            a_state = hashlib.sha256(b"".join(stream_a)).digest()
            b_state = hashlib.sha256(b"".join(stream_b)).digest()
            gap_vector = bytes(x ^ y for x, y in zip(a_state, b_state))

            channel = "W" if curr.zone_type == "W_gap" else "RGB"

            if channel == "W":
                interp = (
                    f"Step {curr.start}: agent A has actions {[ACTION_NAMES.get(x,x) for x in curr.agent_a_actions if x]} "
                    f"that agent B doesn't (or vice versa). "
                    f"One agent believes these actions EXIST in the solution, the other doesn't. "
                    f"The gap vector points toward the belief neither has tested."
                )
            else:
                interp = (
                    f"Step {curr.start}: both agents use similar actions but sequence them differently. "
                    f"A: {[ACTION_NAMES.get(x,x) for x in curr.agent_a_actions if x]} | "
                    f"B: {[ACTION_NAMES.get(x,x) for x in curr.agent_b_actions if x]}. "
                    f"The gap vector encodes the permutation between their orderings."
                )

            frontiers.append(FrontierPoint(
                step=curr.start,
                zone_before=prev,
                zone_after=curr,
                gap_vector=gap_vector,
                channel=channel,
                interpretation=interp,
            ))

    # Summary stats
    n_coherent = sum(1 for z in zones if z.zone_type == "coherent")
    n_w = sum(1 for z in zones if z.zone_type == "W_gap")
    n_rgb = sum(1 for z in zones if z.zone_type == "RGB_gap")
    total = len(zones)
    coherent_frac = n_coherent / total if total else 0

    dominant = "coherent"
    if n_w > n_rgb and n_w > n_coherent: dominant = "W"
    elif n_rgb > n_w and n_rgb > n_coherent: dominant = "RGB"
    elif n_w == n_rgb and n_w > 0: dominant = "mixed"

    total_sigma = sum(z.sigma for z in zones)

    summary = (
        f"{level_id}: {total} zones — {n_coherent} coherent, {n_w} W-gap, {n_rgb} RGB-gap. "
        f"Coherent fraction: {coherent_frac:.0%}. "
        f"{len(frontiers)} frontier(s) detected. "
        f"Dominant channel: {dominant}."
    )

    return PolarityMap(
        level_id=level_id,
        zones=zones,
        frontiers=frontiers,
        coherent_fraction=coherent_frac,
        dominant_channel=dominant,
        total_sigma=round(total_sigma, 4),
        summary=summary,
    )


# ── Hypothesis synthesis ──────────────────────────────────────────────────────

def synthesize_hypothesis(
    frontier: FrontierPoint,
    full_plan_a: list[int],
    full_plan_b: list[int],
) -> Hypothesis:
    """
    At a frontier point, generate a candidate action sequence for a third agent.

    Logic:
    - At W-gap frontiers: one agent has actions the other doesn't.
      Candidate = try what A does but in B's ordering (or vice versa).
      This tests the hypothesis that the action EXISTS but the sequencing was wrong.

    - At RGB-gap frontiers: same actions, different order.
      Candidate = try the ordering that neither used: reverse B relative to A's known-good prefix.
      This triangulates the correct order from both agents' imperfect guesses.
    """
    step = frontier.step
    a_next = full_plan_a[step:step + 5] if step < len(full_plan_a) else []
    b_next = full_plan_b[step:step + 5] if step < len(full_plan_b) else []

    if frontier.channel == "W":
        # W-gap: existence disagreement
        # Candidate: the union of unique actions from both
        a_set = set(a_next)
        b_set = set(b_next)
        only_in_a = a_set - b_set
        only_in_b = b_set - a_set
        shared = a_set & b_set

        # Start with shared (confirmed), then try A-only, then B-only
        candidate = []
        for act in a_next:
            if act in shared and act not in candidate:
                candidate.append(act)
        for act in a_next:
            if act in only_in_a and act not in candidate:
                candidate.append(act)
        for act in b_next:
            if act in only_in_b and act not in candidate:
                candidate.append(act)

        confidence = len(shared) / max(len(a_set | b_set), 1)
        reasoning = (
            f"W-gap at step {step}. "
            f"A uses {[ACTION_NAMES.get(x,x) for x in only_in_a]}, "
            f"B uses {[ACTION_NAMES.get(x,x) for x in only_in_b]}, "
            f"both use {[ACTION_NAMES.get(x,x) for x in shared]}. "
            f"Candidate: execute shared actions first (confirmed), then test A-unique, then B-unique. "
            f"This explores the action space that neither has confirmed."
        )

    else:
        # RGB-gap: ordering disagreement
        # Candidate: try the order that neither used
        # Specifically: interleave A and B's orderings
        candidate = []
        seen = set()
        for pair in zip(a_next, b_next):
            for act in pair:
                if act not in seen:
                    candidate.append(act)
                    seen.add(act)
        # Append anything remaining
        for act in a_next + b_next:
            if act not in seen:
                candidate.append(act)
                seen.add(act)

        confidence = 0.5  # ordering hypotheses are always uncertain
        reasoning = (
            f"RGB-gap at step {step}. "
            f"A orders: {[ACTION_NAMES.get(x,x) for x in a_next]}, "
            f"B orders: {[ACTION_NAMES.get(x,x) for x in b_next]}. "
            f"Candidate: interleaved ordering — neither A's nor B's sequence, "
            f"but the sequence that covers both models' constraints simultaneously."
        )

    return Hypothesis(
        frontier_step=step,
        channel=frontier.channel,
        agent_a_next=a_next,
        agent_b_next=b_next,
        candidate=candidate[:8],  # cap at 8 actions
        confidence=round(confidence, 3),
        reasoning=reasoning,
    )


